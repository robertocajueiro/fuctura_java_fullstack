package classroom1601024;

public class OperadoresDeAtribuicao {

	public static void main(String[] args) {

		double valor = 5;
		System.out.println(valor);
		valor += 2;
		System.out.println(valor);
		valor -= 3;
		System.out.println(valor);
		valor *= 3;
		System.out.println(valor);
		valor /= 2;
		System.out.println(valor);
		valor %= 3;
		System.out.println(valor);
	}

}

package classroom16001024;

public class OperadoresAritmeticos {

	public static void main(String[] args) {

		// + adição
		// - subtração
		// * multiplicacao
		// / divisão
		// % resto
		
		int primeiroValor = 8;
		int segundoValor = 3;
		
		int soma = primeiroValor + segundoValor; // 11
		int sub = primeiroValor - segundoValor; // 5
		int mult = primeiroValor * segundoValor; // 24
		double div = (double) primeiroValor / segundoValor; // 2,7
		int resto = primeiroValor % segundoValor; // 2
		
		System.out.println(soma);
		System.out.println(sub);
		System.out.println(mult);
		System.out.println(div);
		System.out.println(resto);
		
	}

}
